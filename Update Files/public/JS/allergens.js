var milkCount = 1;
var milk = true;
function setPrefMilk()
{
     const milkColor = document.querySelector(".milk");
     if (milkCount == 1){
          milkColor.style.background = "#EE6C4D";
          milk = false;
          milkCount = 2;
          console.log("Set orange");
     }
     else if (milkCount == 2){
          milkColor.style.background = "#5DA271";
          milk = true;
          milkCount = 1;
          console.log("Set green");
     }   
}



var milk = true;
function setPreferenceMilk()
{
   if (document.querySelector(".milk").style.background = "#5DA271") {
        document.querySelector(".milk").style.background = "#EE6C4D";
        milk = false;
   }
   else if(document.querySelector(".milk").style.background = "#EE6C4D") {
         document.querySelector(".milk").style.background = "#5DA271";
        milk = true;
    }
   
} 

